/** Busca uma divisão pelo código exato (ex: "D-1") */
export function findDivisionByCode(code: string): OccupancyDivision | undefined {
  const normalized = code.toUpperCase().trim();
  return OCCUPANCY_DIVISIONS.find((d) => d.division === normalized);
}

/** Retorna todas as divisões de um grupo (ex: "D") */
export function findDivisionsByGroup(group: string): OccupancyDivision[] {
  const normalized = group.toUpperCase().trim();
  return OCCUPANCY_DIVISIONS.filter((d) => d.group === normalized);
}
